#pragma config(Sensor, dgtl6,   armLimit,        sensorTouch)
#pragma config(Sensor, dgtl8,   freqSelect,      sensorDigitalOut)
#pragma config(Sensor, dgtl9,   exposeCtrl,      sensorDigitalOut)
#pragma config(Sensor, dgtl10,  counterReset,    sensorDigitalOut)
